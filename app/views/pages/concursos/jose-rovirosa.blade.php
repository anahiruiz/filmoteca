	<p>A través de la historia, los documentalistas mexicanos han logrado capturar mediante sus cámaras las distintas etapas de la historia de México y con ella sus múltiples rostros, con lo cual lograron adquirir relevancia a lo largo de los años, ofreciéndole al mundo imágenes de los acontecimientos más importantes de la historia de México desde la Revolución mexicana hasta los movimientos estudiantiles que significaron un parte aguas político y sociocultural a nivel mundial. Asimismo retratan tradiciones, problemas sociales y temas de interés que han logrado despertar el interés de cineastas y público en general en los cineclubes, festivales, muestras y retrospectivas nacionales e internacionales. Su actual auge y trascendencia no son insólitos ni gratuitos. </p><br>

