	<h1>Premio José Rovirosa</h1>

	<p>En julio de 1997, pocos meses después de la muerte del cineasta José Rovirosa, la Dirección General de Actividades Cinematográficas de la UNAM y el Centro Universitario de Estudios Cinematográficos (CUEC) instauraron el Premio José Rovirosa al Mejor Documental Mexicano, con la finalidad de impulsar los trabajos de calidad y ratificar su compromiso con la difusión del cine documental en México.</p> <br>

