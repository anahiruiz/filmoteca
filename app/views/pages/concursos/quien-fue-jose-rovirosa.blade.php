<h2>¿Quién fue José Rovirosa Macías?</h2>
	<br>
	<p>Nació en Orizaba, Veracruz, en 1934. Estudió en el Centro Universitario de Estudios Cinematográficos (CUEC), donde se desempeñó posteriormente como Secretario Administrativo (1970-1978), Director (1978-1985), Secretario Técnico (1991-1993) y miembro del Comité Editorial (1992-1997), además de profesor titular desde 1972, de diferentes materias, tanto en el CUEC como en el Centro de Capacitación Cinematográfica (CCC) y otras escuelas.</p><br>

	<p>A lo largo de su carrera realizó 19 documentales, entre los que destacan: Nuestro idioma (1969), Ayautla (1972), Palacio de Minería (1975) y Perdón...investidura (1991), ganador del Ariel de Plata al Mejor Cortometraje de ese año.</p><br>

	<p>Su personalidad multifacética lo llevó a realizar más de doce guiones cinematográficos, varios de los cuales fueron premiados. Y posteriormente, en un intento por contribuir a la difusión del cine en todas sus vertientes fungió, como coordinador de cineclubes (1964-1976), Jefe de Producciones Cinematográficas (1967-1970) en el Departamento de Actividades Cinematográficas de la UNAM y Coordinador Nacional de Cineclubes del IMSS, a principios de la década de los años setenta.</p><br>

	<p>Gracias a su prolífica trayectoria llegó también a desempeñar el cargo de director de la Casa del Lago (1986-1989) y Secretario Docente en la Escuela Internacional de Cine y Televisión (EICTV) de San Antonio de los Baños, Cuba (1989-1990). Además de realizar diversas colaboraciones en periódicos y revistas, es autor del libro Miradas a la realidad, editado por el CUEC, que contiene entrevistas a documentalistas mexicanos.</p><br>

	<p>Así mismo, participó en varias instancias e instituciones como la Fundación de Cineastas, la Academia Mexicana de Ciencias y Artes Cinematográficas, entre otras. Y en 1993, José Rovirosa fue distinguido como becario en la categoría de Creadores de Arte en Medios Audiovisuales por el Consejo Nacional para la Cultura y las Artes. En 1997 se instauró en su honor la entrega del premio José Rovirosa a Mejor documental y en 2008 se realizó una extensión del premio dando surgimiento al premio a Mejor Documental Estudiantil.</p><br>
