<h1>Concurso de crítica cinematográfica Fósforo Alfonso Reyes</h1>

<h2>Convocatoria</h2>
<p>
La Universidad Nacional Autónoma de México, a través de la Facultad de Ciencias Políticas y Sociales y la Dirección General de Actividades Cinematográficas, invitan al Concurso de crítica cinematográfica
</p>

<h2>Bases:</h2> 

<h3>De los participantes</h3>
Participan los estudiantes de la UNAM en estas categorías: 

<ul>
	<li>a) Bachillerato</li>
    <li>b) Licenciatura</li>
    <li>c) Posgrado.</li>
    <li>d) Ex-alumnos de licenciatura.</li>
</ul> 

<h3>De los trabajos</h3>
<p>
El tema de los trabajos es libre pero deberá estar relacionado con las películas seleccionadas por el FICUNAM, que serán proyectadas en la sala Isabel y Ricardo Pozas de la Facultad de Ciencias Políticas y Sociales, únicamente para los inscritos en este concurso, quienes deberán solicitar su inscripción y acceso previamente bajo un seudónimo.
</p>
<p>
Ensayo de crítica cinematográfica de cuatro a seis cuartillas (incluida bibliografía). El trabajo versará sobre la película que el concursante escoja y deberá incluir la ficha técnica de ésta. El trabajo presentará los argumentos que sustentan los juicios de valor, estéticos y dramáticos sobre la película seleccionada. (Las citas, las notas al pie de página y la bibliografía son optativas.)
</p>
<p>
Los trabajos deberán ser originales, firmados con seudónimo y no haber sido publicados. Deberán entregarse dentro de un sobre tamaño oficio en original y dos copias, escritos en computadora (o en máquina de escribir), a doble espacio y con letra Arial 12. Los trabajos que contengan faltas de ortografía serán descalificados. En el sobre se rotulará la siguiente información: 
</p>
<ul>
<li>a) Título del trabajo; </li>
<li>b) Categoría en que concursa; y </li>
<li>c) Seudónimo del autor.</li>
</ul>
Dentro del sobre deberá incluirse otro sobre cerrado, de menor tamaño, en el que se especifiquen los siguientes datos: 
<ul>
<li>a) Título del trabajo;</li>
<li>b) Categoría en que concursa;</li>
<li>c) Nombre completo del autor y seudónimo;</li> 
<li>d) Nombre de la escuela a la que pertenece y número de cuenta o matrícula del nivel que cursa. En su caso, copia de la credencial de ex alumno; y 
<li>e) Domicilio (calle, número, colonia, código postal, delegación o municipio, ciudad y estado), teléfonos (fijo y/o celular) y dirección de correo electrónico.</li>
</ul>

<h3>De la recepción de trabajos</h3>
<p>
La recepción de trabajos será en la División de Educación Continua y Vinculación de 10:00 a 15:00 horas y de 17:00 a 20:00 horas. (Edificio G, Planta Baja) de la Facultad de Ciencias Políticas y Sociales de la UNAM, Circuito Mario de la Cueva S/N, Ciudad Universitaria, C.P. 04510, Delegación Coyoacán, México D.F. La fecha límite de entrega es el lunes 14 de febrero del 2011. En el caso de los trabajos que sean enviados por correo ordinario, se tomará en cuenta la fecha del matasellos postal. Los trabajos no serán devueltos al término del Concurso, ni se enviarán acuses de recibo.
</p>

<h3>De los premios</h3>
Se otorgará sólo un primer lugar en cada categoría: 
<ul>
<li>a) Bachillerato: Laptop, software para edición (video y fotografía), paquete de libros y de películas.</li> 
<li>b) Licenciatura: Laptop, software para edición (video y fotografía) paquete de libros y de películas.</li> 
<li>c) Posgrado: Laptop, software para edición (video y fotografía) paquete de libros y de películas.</li> 
<li>d) Ex alumnos: Laptop, software para edición (video y fotografía) paquete de libros y de películas.</li>

<p>
Los cuatro premios incluyen la publicación de los trabajos ganadores en la revista Punto de Partida, en el entendido de que los autores de los trabajos ganadores cederán todos los derechos de publicación a la UNAM. El jurado otorgará menciones honoríficas a los trabajos que considere relevantes en cada categoría y también recibirán un reconocimiento.
</p>

<h3>Del jurado y los resultados del concurso</h3>
<p>
El jurado del Concurso estará compuesto por académicos, funcionarios universitarios, cineastas y personalidades de la crítica cinematográfica de amplia y reconocida trayectoria. Su fallo será inapelable y se dará a conocer directamente a los ganadores. Los resultados del Concurso se publicarán en el marco de las actividades del Festival Internacional de Cine UNAM (FICUMAN), así como en las páginas electrónicas de la Facultad de Ciencias Políticas y Sociales, de su División de Educación Continua y Vinculación, de la Dirección General de Actividades Cinematográfica y del FICUNAM. De suceder casos no previstos en esta convocatoria, los organizadores del Concurso serán responsables de darles solución.
</p>

<h3>La ceremonia de premiación</h3> 
<p>
La ceremonia de premiación será realizada en el marco del Festival Internacional de Cine UNAM, en lugar y fecha por confirmar.
</p>
