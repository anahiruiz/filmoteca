<h1>Concurso Corto Móvil</h1>

	<p>
	La Universidad Nacional Autónoma de México, a través de la Coordinación de Difusión Cultural y de la Dirección General de Actividades Cinematográficas, invita a participar en el primer concurso Corto Móvil, en el que estudiantes inscritos a nivel medio superior y superior podrán presentar cortometrajes realizados con cámaras de celular, cámaras digitales o una tableta.
	</p>
		<ul>
			<li>Los cortos deberán ser originales y con una duración máxima de dos minutos (incluidos los créditos), grabados en la mejor resolución posible y con un tamaño máximo de 10 MB, en cualquiera de los siguientes formatos: 3GP, MP4, MPG, FLV, AVI, MOV y MKV.</li>
			<li>El tema es libre y los cortos premiados serán exhibidos en la página web y redes sociales de la Filmoteca UNAM.</li>
			<li>Se otorgará un premio único, que consiste en una tableta iPad de 16 GB. Los realizadores del corto ganador y de las menciones honoríficas recibirán una beca para el Taller de cortometrajes con dispositivos móviles, que se llevará a cabo el primer semestre de 2014.</li>
			<li>Cierre de inscripciones: 15 de noviembre de 2013.</li>
			<li>Para ver más: http://culturacolectiva.com/concurso-corto-movil-de-la-unam/#sthash.YWM03UuE.dpuf</li>
			<li>Podrán participar todos aquellos estudiantes que comprueben estar inscritos a nivel medio superior y superior al momento de registrar sus trabajos.</li>
		</ul>

<h2>Bases</h2>
	<ul>
		<li>Los cortometrajes presentados deberán estar titulados y sólo podrán estar grabados con la cámara de un teléfono móvil, cámara fotográfica digital o tableta (tablet), pudiendo luego, si se desea, editarse con herramientas externas.</li>
		<li>Los cortos deben ser originales, con una duración máxima de dos minutos, incluidos los títulos de crédito si los hubiere, y grabados en la máxima resolución y calidad posibles, con un tamaño máximo de 10 megas. El formato puede ser 3GP, MP4, MPG, FLV, AVI, MOV, MKV.</li>
		<li>La temática de los cortos será libre. No obstante, la organización del concurso se reserva el derecho de excluir todas aquellas obras cuyo contenido vulnere algún derecho fundamental de la personas.</li>
		<li>Las obras deberán estar en español o subtituladas en español.</li>
		<li>Las copias de las obras inscritas pasarán a formar parte del acervo de la Filmoteca de la UNAM para su consulta y para todas aquellas actividades culturales relacionadas con la difusión de este concurso. Para ello, los concursantes deberán firmar de conformidad el documento que se descarga aquí.</li>
		<li>Las obras se enviarán junto con el formulario de inscripción que se descarga aquí y el documento de conformidad antes mencionado al correo: cortomovil.filmoteca@gmail.com.</li>
		<li>Se recibirán trabajos hasta el 15 de noviembre de 2013.</li>
		<li>Se otorgará un premio único que consiste en una tableta iPad de 16 GB.</li>
		<li>El jurado estará integrado por especialistas propuestos por la institución convocante.</li>
		<li>Los premios pueden quedar desiertos si el jurado así lo decide. El fallo del jurado será inapelable.</li>
		<li>Los realizadores del corto ganador y de las menciones honoríficas recibirán una beca para el "Taller de cortometrajes con dispositivos móviles", que se llevará a cabo el primer semestre de 2014.</li>
		<li>Los resultados se publicarán en enero de 2014 en la página web www.filmoteca.unam.mx y en las redes sociales de la Filmoteca UNAM.</li>
		<li>Los cortos premiados serán exhibidos en la página web y redes sociales de la Filmoteca UNAM. - See more at: http://culturacolectiva.com/concurso-corto-movil-de-la-unam/#sthash.YWM03UuE.dpuf</li>
	</ul>
