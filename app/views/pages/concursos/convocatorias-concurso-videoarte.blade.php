	<h1>CONCURSO DE VIDEOARTE</h1>

		<p align="center">La Universidad Nacional Autónoma de México a través del Museo Universitario Arte Contemporáneo, la Facultad de Ciencias Políticas y Sociales y la Dirección General de Actividades Cinematográficas, Filmoteca UNAM</p>
		<p align="center"><strong>CONVOCAN</strong></p>

		<p align="center">al <strong>Concurso Nacional de Videoarte Universitario Visiones del arte</strong></p>

		<p align="right"><i>Se podría definir formalmente al videoarte como cualquier obra con intencionalidades artísticas que emplea, en sus procesos y como soporte, a los instrumentos o dispositivos propios del video.</i></p>
		<p align="right"><i>Gilles Charalambos</i>

		<p align="right"><i>El cine sería la épica, la televisión la novela y el videoarte la poesía</i></p>
		<p align="right"><i>Pola Weiss</i></p>

		
		<p>La masificación de la cámara de video expandió las posibilidades narrativas y formales de la producción artística. La capacidad de registrar el mundo y manipular material audiovisual permitió conformar lenguajes y lógicas de creación alternativas a la de la cultura de masas, regulada por el cine y la televisión. Hoy en día, cinco décadas después, el video se ha convertido en una herramienta al alcance de la mayoría con la aparición de cámaras miniatura en dispositivos móviles y plataformas de distribución de videos caseros en los medios electrónicos que han reformulado las condiciones de realización de video en la actualidad.</p>

		<p>Esta convocatoria tiene la intención de contribuir al estímulo de la producción de videoarte en México iniciada en los años setenta, impulsando a los nuevos creadores y abriendo un espacio de encuentro entre artistas de distintas generaciones para seguir reflexionando sobre los significados y posibilidades que plantea esta práctica.</p>

		<p>Para esta 5a edición te invitamos a realizar una pieza de videoarte en la que destaquen la reflexión, experimentación y creación artística a partir de los procesos y soportes videográficos.</p>

		<p><strong>PARTICIPANTES</strong></p>
			<ul>
				<li>Esta convocatoria está abierta a estudiantes de bachillerato, licenciaturas y posgrados, así como a personas o artistas de formación independiente interesadas en la creación artística a través del video.</li>
				<li>Se recibirán trabajos individuales y colectivos.</li>
				<li>Cada autor o colectivo podrá presentar un máximo de 3 obras.</li>
			</ul>
		 

		<p><strong>CATEGORÍAS</strong></p>
			<ul>
				<li>Preparatorias y Bachilleratos.</li>
				<li>Licenciaturas y Posgrados.</li>
				<li>Formación independiente (Productores o realizadores autodidactas o con una formación no institucional).</li>
			</ul>
		 

		<p><strong>OBRAS</strong></p>
			<ul>
				<li>Es requisito que los trabajos sean originales y no hayan sido proyectados con anterioridad.</li>
				<li>Los trabajos deberán tener una duración máxima de 10 minutos.</li>
				<li>Los videos deberán subirse a un canal de Youtube o Vimeo personal en configuración PRIVADO en formato HD 1920 x 1080 (16:9).</li>
				<li>El link de visualización deberá ser enviado junto con la contraseña de acceso al correo  concurso.videoarte@muac.unam.mx</li>
				<li>Todos los trabajos deberán incluir una cortinilla en letra Arial no. 12 con la siguiente información: nombre(s) autor(es), título y categoría.</li>  
			</ul>


