				<li>2 imágenes still del video (tiff, png, jpeg, 300 dpi) con fines de difusión.</li>
				<li>NOTA: cada pieza deberá enviarse en un correo electrónico diferente.</li>
				<li>La convocatoria estará abierta del 1 de junio al 30 de octubre del 2015.</li>
			</ul>
		 

