  <br>
  <h2>TALLER FUNDAMENTOS DE GUIÓN PARA CINE DOCUMENTAL</h2>
   <strong><p>Impartido por: José Luis Mariño López</p>
</strong>


    <p align="right"><i>El guión es escritura y reescritura. Más allá de ser la primera etapa de una película, es el eje de ésta y sigue un proceso permanente de re-escritura en todas las fases de producción.
    Esto porque la fotografía y la cámara narran, porque se escribe con la luz, se cuenta con ambientes y atmósferas, con ruidos y sonidos... Es decir, el guión es una escritura que no concluye cuando se pone fin en el impreso sino cuando una estética le da forma a la narración allí planteada, la reinventa.</i></p>
    <p align="right">Lucía Victoria Torres</p>
    <br>

    <p>Éste es un taller introductorio cuya meta general es presentar y desarrollar en la práctica elementos fundamentales del guión para cine documental, entendiendo al guión como al parte esencialmente creativa, de desarrollo de posibilidades, de exploración de escenarios y de empleo de recursos de la preproducción de una película documental, que no sólo debe cubrir apropiadamente los requisitos técnicos, logísticos y metodológicos; también debe sondear -y a ello se dedica este taller- las perspectivas centrales y derivadas del tema elegido, las ideas visuales y sonoras, el diseño de la profundización temática, la composición de la narrativa, el ritmo, los sujetos protagónicos, las estructuras discursivas; en suma, esbozar el estilo y función de la obra, sea que ésta se adhiera a un esquema preciso de rodaje o cuya resolución principal se dé en el proceso de edición - postproducción.</p>
    <br>

    <p align="right"><i>Al guión podríamos llamarlo también una guía grande, abierta a las sucesivas reescrituras que ocurren cuando la investigación avanza, cuando acontece el registro, cuando se piensa en montaje, cuando el público debate y rediscute el film.</p></i>
    <p align="right">Claudio Remedi</p>


    <p><strong>Requisito:</strong> contar un tema definido para desarrollar durante el Taller.</p>

    <p><strong>OBJETIVOS GENERALES</p></strong>

        <ul>
          <li>Conocimiento del guión para cine documental y aprendizaje de su teoría, práctica y técnicas.</li>
          <li>Familiarización con los esquemas básicos para guión de cine documental.</li>
          <li>Apreciación de las estructuras organizativas y narrativas en diversos ejemplos de estilos de cine documental.</li>
          <li>Introducción a la lectura profunda de películas documentales.</li>
        </ul>

    <p><strong>OBJETIVOS ESPECÍFICOS</p></strong>

        <ul>
          <li>Planteamiento y desarrollo de temas individuales en un taller colectivo.</li>
          <li>Conocimiento y manejo de textos básicos sobre el tema.</li>
          <li>Análisis de estructuras informativas y narrativas.</li>
          <li>Empleo de las herramientas técnicas y teóricas proporcionadas.</li>
          <li>Análisis en pantalla de diversos tipos de guiones y estructuras de cine documental.</li>
        </ul>

    <p><strong>METODOLOGÍA<p></strong>

        <p>Los participantes trabajarán en proyectos basados en temas propios de tal manera que de cada tema se configure un proyecto diferente a los demás, aunque se parta de una misma base.</p>

        <p>Puesto que un objetivo es desarrollar las habilidades de observación, descripción, asociación, selección, abstracción, síntesis y exposición, el trabajo en aula es una constante. Para ello se recurre a una metodología de análisis de fragmentos de cine documental a partir de los elementos proporcionados en clase y en las lecturas.</p>

        <p>Otra característica fundamental es la presentación de temas que serán objeto de desarrollo a lo largo del curso, en un formato de taller que se basa en la discusión colectiva de las ideas, temas, proyectos, siempre en la perspectiva de aportar para la mejora.</p>

        <p>Así, de forma dinámica, se presentan los temas, por una parte, y, por otra, se conocen conceptos y técnicas que se emplean en el desarrollo de los temas, con el fin de hacer más práctica y perdurable la incorporación de herramientas y conceptos.</p>

        <p>De forma paralela se maneja un proceso de guión hipotético que funciona como testigo para efecto de ejemplificar el proceso completo.</p>

        <p>Se manejan una bibliografía y una filmografía básicas y se hacen visionamientos guiados de películas documentales seleccionadas de acuerdo a los temas de los proyectos. Asimismo se cuenta con un blog que concentre documentos, textos, enlaces con sitios de interés y comentarios de los talleristas.</p>

        <p align="right"><i>El proceso más importante a partir del momento en que se cuenta con un concepto original y hasta que la película es terminada; esto es, la organización de las ideas.</p></i>
        <p align="right">Russell Porter</p>

        <p>El curso se desarrolla en 12 sesiones de 3 horas cada una y se compone de tres unidades.</p>

    <ol>
      <li>Introducción al guión para cine documental, taller, objetivos y procedimientos. 1 sesión, 3 horas</li>
      <li>Construcción de un guión para cine documental. Del tema a la idea, del idea al proyecto, del proyecto al producto. 10 sesiones, 30 horas</li>
        <ul>
          <li>a. La preproducción</li>
          <li>b. Tema - planteamiento de temas personales y tema testigo</li>
          <li>c. Profundización del tema - ideas de investigación</li>
          <li>d. Sinopsis</li>
          <li>e. Escaleta por bloques y secuencias</li>
          <li>f. Propuesta general</li>
          <li>g. Nociones de una carpeta de producción documental</li>
        </ul>

      <li>Presentación y comentarios de proyectos desarrollados. 1 sesión, 3 horas</li>
    </ol>
