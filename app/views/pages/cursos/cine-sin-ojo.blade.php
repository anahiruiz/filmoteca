  <h2>SEMINARIO CINE SIN OJO. DEL CINEMATÓGRAFO A LA IMAGEN 3D</h2>
   <strong><p>Impartido por: Mtro. Lauro López Sánchez</p>
</strong>

    <p><strong>Descripción del programa</strong></p>

    <p>El seminario busca trazar una arqueología sobre la cuestión técnica e ideológica en el desarrollo de los medios de reproducción como el cine y la cámara fotográfica desde sus inicios. Se analizarán ejemplos concretos de cómo la mirada se ha desplazado poco a poco y ha sido suplantada por un complejo sistema de vigilancia de seguridad militar y policiaco.  Ahora se cuenta con un sistema tecnológico hibrido desplegado en todos los niveles de la vida, donde la realidad se asocia, se organiza y determina.  Esto conlleva a toda una problemática ontológica, ya que replantean el lugar de la producción de la mirada y el lugar del espectador.</p>

    <p><strong>Objetivos:</strong> Trazar nuevos marcos conceptuales entorno a la imagen contemporánea, tales como el cine sin ojo.  En el seminario se darán ejemplos concretos y se reflexionará sobre ellos para desarrollarlos en las distintas prácticas artísticas visuales. Realizar planteamientos críticos que permitan al alumno distinguir los dispositivos ideológicos en el ilimitado desarrollo de imágenes y tecnologías de la imagen.</p>

    <p><strong>Justificación.</strong> Los planteamientos del curso, así como los temas trazan una línea conceptual histórica desde el nacimiento técnico de la fotografía hasta los aspectos militares entorno a los drones. Mediante esto, el alumno podrá trazar sus propias cartografías conceptuales dependiendo de sus propias prácticas y disciplinas.  Formulará sus propias soluciones y conceptos entorno a las temáticas planteadas. También, durante el curso tendrá acceso a material visual de difícil acceso. Dirigido a todo aquél artista o estudiante interesado en la producción de la imagen contemporánea, así como los dispositivos tecnológicos y sus aspectos ideológicos.</p>

    <p><strong>Beneficios, logros esperados y valor agregado generado por el programa.</strong> El alumno podrá trazar sus propias cartografías conceptuales dependiendo de sus propias prácticas y disciplinas.  Formulará sus propias soluciones y conceptos entorno a las temáticas planteadas. También, durante el curso tendrá acceso a material visual de difícil acceso.</p>

    <p><strong>Dirigido:</strong> A todo aquel artista o estudiante interesado en la producción de la imagen contemporánea así como los dipositivos tecnológicos y sus aspectos ideólogicos.</p> 

    <p><strong>TEMARIO</strong></p>

    <p><strong>Primer módulo</strong></p>

    <ul>
      <li>Introducción</li>
      <li>Planteamientos técnicos del siglo XX.</li>
      <li>La historia como cinematografía.</li>
    </ul>

    <p><strong>Segundo módulo</strong></p>

    <ul>
      <li>Giros dialécticos de la imagen.</li>
      <li>Manifiesto Kino Prada.</li>
      <li>Concepto de historia en Walter Benjamin.</li>
      <li>Tecnología/capital.</li>
    </ul>

    <p><strong>Tercer módulo</strong></p>

    <ul>
      <li>La mirada cinematográfica de las grandes ideologías.</li>
      <li>Hitler cineasta, o la voluntad del poder visual.</li>
    </ul>

    <p><strong>Cuarto módulo</strong></p>

    <ul>
      <li>Tecnología y desarrollo de cámaras portátiles. La Bolex y la guerra de Vietnam.</li>
      <li>Micro revolución visual. El germen político de Isidore Isou, Los Letristas y Guy Debord.</li>
      <li>Recolectores de imágenes ruinas. El Nuevo Cine Americano.</li>
    </ul>

    <p><strong>Quinto módulo</strong></p>

    <ul>
      <li>La imagen histórica de la catástrofe de Alexander Kluge.</li>
      <li>Modelos de control y vigilancia.</li>
      <li>La Dromología de Paul Virilio.</li>
      <li>Nuevas cuestiones antropológicas en la literatura. J.G.Ballard y Don DeLillo.</li>
    </ul>

    <p><strong>Sexto módulo</strong></p>

    <ul>
      <li>Desplazamiento de la mirada.</li>
      <li>La sospecha de Farocki.</li>
      <li>El ojo/máquina.</li>
    </ul>

    <p><strong>Séptimo módulo</strong></p>

    <ul>
      <li>GoPro, análisis del deporte extremo.</li>
      <li>Cyber Materialismo Histórico, o la sobreproducción de la imagen.</li>
      <li>El surgimiento del Leviathan 2.0</li>
    </ul>

    <p><strong>Octavo módulo</strong></p>

    <ul>
      <li>Desarticulando la noción de autor. Godard por Godard.</li>
      <li>El doble o el autor desplazado. Godard sin Godard.</li>
      <li>Apagar la imagen.</li>
    </ul>

