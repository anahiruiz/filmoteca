	<h1>HISTORIAS RECUPERADAS</h1>
  <h2>LAS VIÑAS DE LA IRA </h2>

	<p>SINOPSIS: Libre bajo fianza, Tom Joad se dirige a la casa de sus padres en Oklahoma. Por el camino, encuentra  a Casy  quien ha dejado de ser predicador por haber perdido la fe. La granja familiar está abandonada, la persistente sequía y la especulación financiera los han obligado a abandonar su hogar.  Le informan que toda su familia ha decidido emigrar a California debido a que, en un volante publicitario, han leído que allá hay mucho trabajo. Los encuentra justo a tiempo para partir con ellos.  Después de muchas penalidades, llegan a California para descubrir que los lugares de trabajo son escasos y que la competencia para ocuparlos es feroz. Los dueños recurren a guardias blancas para imponer sus condiciones, llegando incluso a disparar contra los trabajadores. Casy ha recuperado su fe, pero ahora, como organizador de los obreros agrícolas, lucha para que los trabajadores no tengan que sufrir todo tipo de humillaciones por un mísero salario. A causa de esta lucha, lo asesinan. En ese momento, Tom está con él y logra matar a uno de los agresores. Su familia lo esconde, pero la policía está cerca. Tom decide separarse de su familia. Al despedirse de su madre, Tom le dice que espera volverla a encontrar algún día, mientras él se dedicará a organizar a los trabajadores. Así honrará la memoria de su amigo. La familia de Tom se dirige rumbo al norte, su madre ha recuperado la esperanza y se dispone a enfrentar la vida con ánimo renovado. </p>
	<br>
	<p>COMENTARIO: Hoy consideramos a John Ford como el mejor director de westerns que ha habido en toda la historia, pero esto no siempre fue así. Desde mediados de los años treinta hasta que los Estados Unidos se involucraron en la Segunda Guerra Mundial, Ford dirigió una serie de dramas sociales que respondían muy puntualmente a la difícil situación por la que atravesaba su país. Desde 1935, en que dirigió El Delator, película destinada a resaltar la difícil lucha de los irlandeses por obtener su independencia, hasta 1941, año en que se dedica a describir muy detalladamente las penalidades que sufrían los mineros galeses en Que verde era mi valle, John Ford canalizó su indudable talento en presentar una serie de retratos de los más afectados por esos conflictos: los ciudadanos comunes y corrientes.
  <p>No  cabe duda  que  Las viñas de la ira, rodada a finales de 1939, es su más lograda incursión en el género. Este retrato realista, dedicado a seguir a esa multitud de seres humanos que tuvieron que salir huyendo de la crisis financiera que siguió a la quiebra generalizada de la Bolsa de Valores en 1929 y del desastre ecológico que representó la persistente sequía en el Medio Oeste americano, es una muy lograda adaptación de la novela homónima de John Steinbeck y una muestra del uso político-ideológico que se puede hacer del cine.</p>
  <p>Contaremos con una excelente copia en 35 mm de la película, tirada a partir del negativo original, gracias a la colaboración del Archivo Fílmico de la Universidad de Harvard a quienes extendemos una vez más nuestro más caluroso agradecimiento por este préstamo.</p>

	

