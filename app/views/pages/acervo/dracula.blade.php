	<h2>DRÁCULA</h2>

	<p>SINOPSIS: 
	A las doce, en la noche de Walpurgis, Renfield, el empleado inglés de un despacho de abogados, llega al castillo del Conde Drácula, en los Cárpatos. Drácula, después de firmar los papeles para adquirir una abadía en ruinas, cerca de Londres, enloquece a Renfield con su mirada y le ordena obediencia. Renfield escolta el féretro que contiene tierra de los Cárpatos, donde yace Drácula durante el día, mientras viaja en un barco que lo lleva a Londres. Los tripulantes del barco mueren misteriosamente. Ya en la ciudad, el Conde se presenta a la sociedad londinense y conoce a su vecino el doctor Seward, quien dirige un asilo de enfermos mentales. Drácula hace amistad rápidamente con Lucía Weston, amiga de la familia, y a continuación comienza su asalto a Eva Seward, la hija del médico. Un invitado, el experto en ocultismo Van Helsing, reconoce a Drácula y sus poderes. De este modo, se desata una batalla entre Drácula y Van Helsing por el cuerpo y alma de Eva.  Es predecible el final, el “happy ending”, con la muerte y desaparición del Conde Drácula.</p>
