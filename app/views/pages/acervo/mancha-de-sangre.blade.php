	<h1>HISTORIAS RECUPERADAS</h1>
  <h2>LA MANCHA DE SANGRE</h2>

	<p>SINOPSIS: Una joven se hace cabaretera y es explotada por un padrote celoso y cínico; pero la misma se enamora de un tímido joven fuereño recién llegado a la ciudad. La mancha de sangre es un registro de la vida nocturna en la Ciudad de México, un inventario de los personajes que habitan en el submundo citadino, un desfile de rostros macerados, piernas golpeadas, cuerpos desnudos y miradas libidinosas, es lo que la aguda mirada del pintor y crítico de arte Adolfo Best-Maugard supo plasmar.</p>
	<br>
	<p>COMENTARIO: Filme perdido durante cincuenta años. El negativo original de nitrato de celulosa lo encontramos maltrecho, incompleto y muy rayado.  Los censores, que durante los años treinta secuestraron el filme y lo mutilaron, no solamente lo cortaron sino que lo manipularon de la peor manera: el negativo tiene múltiples rayaduras longitudinales que en pantalla se observan como rayas blancas.  También faltan dos rollos de este negativo: el No. 6 de sonido y el No. 9 de imagen. Pese a esta circunstancia, teníamos que sacar a la luz una película como ésta, con tantas particularidades creativas.  Para aminorar el faltante del rollo 6 de sonido, subtitulamos este rollo con la interpretación del movimiento de los labios de los personajes y en el caso del sonido del rollo 9, consideramos que este es suficiente para entender lo que sucede pero que no podemos ver.  Nunca olvidaremos lo que se escucha al final: “ahora sí seremos felices”.</p>


